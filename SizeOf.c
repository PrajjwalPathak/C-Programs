//Program to print size of different data types

#include<stdio.h>

int main() {
    int a;
    float b;
    double c;
    char d;
    printf("\nSize of int: %d",sizeof(a));
    printf("\nSize of float: %d",sizeof(b));
    printf("\nSize of double: %d",sizeof(c));
    printf("\nSize of char: %d",sizeof(d));

    return 0;
}